    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String maCN = cboMaCN.getSelectedItem().toString();
        String tenCN = txtTenCN.getText();
        String maPC = txtMaPhanCong.getText();
        String maCT = jTextField1.getText();
        String tenCT = jTextField4.getText();
        String soNgayCong = jTextField3.getText();
        DefaultTableModel md = (DefaultTableModel) jTableDSPhanCong.getModel();
        try {
            Connection con = ConnectSQL.getConnection();
            String sql = "INSERT INTO QuanLyNgayCong (MaCongNhan, TenCongNhan, MaPhanCong, TenCongTrinh, MaCongTrinh, SoNgayCong) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement statement = con.prepareStatement(sql);
            statement.setString(1, maCN);
            statement.setString(2, tenCN);
            statement.setString(3, maPC);
            statement.setString(4, tenCT);
            statement.setString(5, maCT);
            statement.setString(6, soNgayCong);
            statement.executeUpdate();
            Object[] rowData = {maCN, tenCN, maPC, tenCT, maCT, soNgayCong + " ngày"};
            md.addRow(rowData);
            JOptionPane.showMessageDialog(null, "Thêm dữ liệu thành công!");
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Lỗi khi thêm dữ liệu!");
        }

        jTableDSPhanCong.setModel(md);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTableDSPhanCong.getSelectedRow();
        if (row >= 0) {
            Connection con = ConnectSQL.getConnection();
            String sql = "DELETE FROM QuanLyNgayCong WHERE MaCongNhan = ?";
            int maCN = Integer.parseInt((String) jTableDSPhanCong.getValueAt(row, 0));
            try {
                PreparedStatement statement = con.prepareStatement(sql);
                statement.setInt(1, maCN);
                statement.executeUpdate();
                DefaultTableModel model = (DefaultTableModel) jTableDSPhanCong.getModel();
                model.removeRow(row);
                JOptionPane.showMessageDialog(null, "Xóa Thành Công");
            } catch (SQLException ex) {
                Logger.getLogger(QuanLyNgayCong.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Lỗi khi xóa dữ liệu");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Chọn hàng cần xóa");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Connection con = ConnectSQL.getConnection();
        DefaultTableModel md = (DefaultTableModel) jTableDSPhanCong.getModel();
        try {
            String sql = "UPDATE QuanLyNgayCong SET TenCongNhan=?, MaPhanCong=?, TenCongTrinh=?, MaCongTrinh=?, SoNgayCong=? WHERE MaCongNhan=?";
            PreparedStatement stmt = con.prepareStatement(sql);

            // Lấy thông tin từ các trường nhập liệu
            String tenCN = txtTenCN.getText();
            String maPC = txtMaPhanCong.getText();
            String tenCT = jTextField4.getText();
            String maCT = jTextField1.getText();
            int soNgayCong = Integer.parseInt(jTextField3.getText());
            int maCN = Integer.parseInt(cboMaCN.getSelectedItem().toString());
           
            // Cập nhật thông tin vào câu truy vấn SQL
            
            stmt.setString(1, tenCN);
            stmt.setString(2, maPC);
            stmt.setString(3, tenCT);
            stmt.setString(4, maCT);
            stmt.setInt(5, soNgayCong);
            stmt.setInt(6, maCN);
            
            // Thực thi câu truy vấn SQL
            int rowsUpdated = stmt.executeUpdate();
            if (rowsUpdated >= 0) {
                JOptionPane.showMessageDialog(this, "Cập nhật thông tin thành công!");
                // Cập nhật lại bảng hiển thị
                md.setValueAt(maCN, jTableDSPhanCong.getSelectedRow(), 0);
                md.setValueAt(tenCN, jTableDSPhanCong.getSelectedRow(), 1);
                md.setValueAt(maPC, jTableDSPhanCong.getSelectedRow(), 2);
                md.setValueAt(tenCT, jTableDSPhanCong.getSelectedRow(), 3);
                md.setValueAt(maCT, jTableDSPhanCong.getSelectedRow(), 4);
                md.setValueAt(soNgayCong + " ngày", jTableDSPhanCong.getSelectedRow(), 5);
                stmt.executeUpdate();
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật thông tin thất bại!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(QuanLyNgayCong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

