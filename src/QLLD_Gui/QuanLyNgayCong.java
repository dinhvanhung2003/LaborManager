    private void jTableSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSPMouseClicked
        int selectedRow = jTableSP.getSelectedRow();
        if (selectedRow >= 0) {
            // Lấy thông tin mã công trình của hàng được chọn
            String maCT = jTableSP.getValueAt(selectedRow, 0).toString();

            // Tìm kiếm danh sách phân công theo mã công trình
            PhanCong_DAO dao = new PhanCong_DAO();
            List<PhanCong> listPC = dao.timTheoMaCongTrinh(maCT);

            // Lọc danh sách nhân viên có cùng mã công trình
            List<CongNhan> listCN = new ArrayList<>();
            // Hiển thị danh sách mã nhân viên và tên công nhân lên combobox
            cboMaCN.removeAllItems();
            for (PhanCong pc : listPC) {
                cboMaCN.addItem(String.valueOf(pc.getMaCN()));
            }

            // Sự kiện khi chọn mã nhân viên trên combobox
            cboMaCN.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    Object selectedItem = cboMaCN.getSelectedItem();
                    if (selectedItem != null) {
                        int maCN = Integer.parseInt(selectedItem.toString());

                        for (PhanCong pc : listPC) {
                            if (pc.getMaCN() == maCN) {
                                txtTenCN.setText(pc.getTenCN());
                                txtMaPhanCong.setText(pc.getMaPC());
                                jTextField4.setText(pc.getTenCongTrinh());
                                jTextField1.setText(pc.getMaCongTrinh());
                                break;
                            }

                        }
                    }
                }
            });
        }
    }//GEN-LAST:event_jTableSPMouseClicked


    private void jTableDSPhanCongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDSPhanCongMouseClicked
        int row = jTableDSPhanCong.getSelectedRow();
        if (row >= 0) {
            // Lấy thông tin mã nhân viên của hàng được chọn
            int maNV = Integer.parseInt(jTableDSPhanCong.getValueAt(row, 0).toString());
            cboMaCN.removeAllItems();

            // Tìm kiếm danh sách phân công theo mã nhân viên
            PhanCong_DAO dao = new PhanCong_DAO();
            List<PhanCong> listPC = dao.timTheoMaCongCongNhan(maNV);

            // Thêm danh sách mã nhân viên vào combobox
            for (PhanCong pc : listPC) {
                cboMaCN.addItem(String.valueOf(pc.getMaCN()));
            }

            // Chọn mã nhân viên tương ứng với hàng được chọn trong jTableDSPhanCong
            cboMaCN.setSelectedItem(maNV);
            txtTenCN.setText(jTableDSPhanCong.getValueAt(row, 1).toString());
            txtMaPhanCong.setText(jTableDSPhanCong.getValueAt(row, 2).toString());
            jTextField4.setText(jTableDSPhanCong.getValueAt(row, 3).toString());
            jTextField1.setText(jTableDSPhanCong.getValueAt(row, 4).toString());
            jTextField3.setText(jTableDSPhanCong.getValueAt(row, 5).toString().replace(" ngày", ""));
        }

    }//GEN-LAST:event_jTableDSPhanCongMouseClicked

