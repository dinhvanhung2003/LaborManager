    private void updateTable(DefaultTableModel model) throws SQLException {
        Connection con = ConnectSQL.getConnection();
        String sql = "SELECT * FROM QuanLyNgayCong";
        PreparedStatement statement = con.prepareStatement(sql);
        ResultSet rs = statement.executeQuery();
        while (rs.next()) {
            String maCN = rs.getString("MaCongNhan");
            String tenCN = rs.getString("TenCongNhan");
            String maPC = rs.getString("MaPhanCong");
            String tenCT = rs.getString("TenCongTrinh");
            String maCT = rs.getString("MaCongTrinh");
            String soNgayCong = rs.getString("SoNgayCong");
            Object[] rowData = {maCN, tenCN, maPC, tenCT, maCT, soNgayCong + " ngày"};
            model.addRow(rowData);
        }
        jTableDSPhanCong.setModel(model);
    }
