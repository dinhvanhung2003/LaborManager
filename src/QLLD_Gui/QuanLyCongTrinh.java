    private void updateTableData() {
        CongTrinh_DAO ds = new CongTrinh_DAO();
        List<CongTrinh> list = ds.docTubang();
        dataModel.setRowCount(0); // Xóa toàn bộ dữ liệu hiện có trong bảng
        for (CongTrinh s : list) {
            String formattedGiaTri = new DecimalFormat("#,### VND").format(s.getTongGiaTriHopDong()); // Format the value of tongGiaTriHopDong
            String[] rowData = {s.getMaHopDong(), s.getMaCongTrinh(), s.getTenCongTrinh(), s.getnQLCT() + "", s.getTenNhanVien(), s.getNgayKi(), s.getNgayHoanThanh(), formattedGiaTri};
            dataModel.addRow(rowData);
        }
        jTableHD.scrollRectToVisible(jTableHD.getCellRect(jTableHD.getRowCount() - 1, 0, false)); // Cuộn tới dòng mới nhất

    }

