    private CongTrinh reverSPFormTextFile() {
        String maHD = txtMaHD.getText();
        String maCongTrinh = txtMaSP.getText().toString();
        String tenCongTrinh = txtTenKH.getText().toString();
        String maNV = cboMaNV.getSelectedItem().toString();
        String tenNV = txtTenNV.getText().toString();
        // Định dạng chuỗi kết quả
        SimpleDateFormat sdf = new SimpleDateFormat("MM/dd/yyyy");
        Date ngayKy = txtNgayKy.getDate();
        Date ngayHT = txtNgayThanhLy.getDate();
        String ngayKyString = sdf.format(ngayKy);
        String ngayHTString = sdf.format(ngayHT);
        // Xóa các ký tự không phải s
        String giaTriString = txtGiaTri.getText().replaceAll("[^\\d]", "");
        long tongGTri = Long.parseLong(giaTriString);
        System.err.println(tongGTri + "rs");
        //Kiem tra du lieu
        if (!maHD.matches("^HD\\d{3}$")) {
            JOptionPane.showMessageDialog(null, "Mã hợp đồng phải bắt đầu bằng HD theo sau là 3 chữ số", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        if (!maCongTrinh.matches("^CT\\d{3}$")&&!dSCT.checktrungma(txtMaSP.getText()))  {
                JOptionPane.showMessageDialog(null, "Mã Công Trình phải bắt đầu bằng CT theo sau là 3 chữ số", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
                
            return null;
        }

        return new CongTrinh(maCongTrinh, tenCongTrinh, ngayHTString, maHD, ngayKyString, new nguoiQuanLiCongTrinh(maNV), tenNV, tongGTri);
    }

    private void updateComboBox() {
        CongNhan_DAO ds = new CongNhan_DAO();
        List<CongNhan> list = ds.docTuBang();
        for (CongNhan cn : list) {

            if (cn.getChucVu().equals("Quản Lý Công Trình")) {
                cboMaNV.addItem(String.valueOf(cn.getMaCN()));
                System.err.println(cn.getMaCN());
            }
        }
        cboMaNV.addItemListener(new ItemListener() {
            public void itemStateChanged(ItemEvent arg0) {
                if (arg0.getStateChange() == ItemEvent.SELECTED) {
                    int selectedMaNV = Integer.parseInt(cboMaNV.getSelectedItem().toString());
                    // tìm kiếm người quản lí công trình tương ứng với mã nhân viên
                    CongNhan selectedCongNhan = null;
                    for (CongNhan cn : list) {
                        int maCN = cn.getMaCN();
                        if (maCN == selectedMaNV) {
                            selectedCongNhan = cn;
                            break;
                        }
                    }
                    if (selectedCongNhan != null) {
                        // hiển thị tên của người quản lí công trình tương ứng
                        txtTenNV.setText(selectedCongNhan.getTenCN());
                    }
                }
            }
        });
    }

