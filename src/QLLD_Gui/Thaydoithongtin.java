    public int laymcn() {
        conn = cn.getConnection();

        String query = "Select MaCongNhan as macn from TaiKhoan where TenTaiKhoan=? ";
        try {
            PreparedStatement st = conn.prepareStatement(query);

            st.setString(1, getUsername());
            ResultSet rs = st.executeQuery();
            while (rs.next()) {
                return rs.getInt("macn");

            }
        } catch (SQLException ex) {
            Logger.getLogger(HomeMain.class.getName()).log(Level.SEVERE, null, ex);
        }

        return -1;
    }

    private void updateData() {
        CongNhan_DAO ds = new CongNhan_DAO();
        int macn = laymcn();
        List<CongNhan> list = ds.getCongNhanTheoMaCN(macn);
        for (int i = 0; i < list.size(); i++) {
            txtDiaChiCN.setText(list.get(i).getDiaChi());
            txtLuongcb.setText(Double.toString(list.get(i).getLuongcb()));
            txtMaCN.setText(Integer.toString(list.get(i).getMaCN()));
            txtPhuCapCN.setText(Double.toString(list.get(i).getPhuCap()));
            txtSDT.setText(list.get(i).getsDT());
            txtCMNDCN.setText(list.get(i).getCMND());
            txtHoTenCN.setText(list.get(i).getTenCN());
            cboChucVu.setSelectedItem(list.get(i).getChucVu());
            Date date;
            try {
                date = new SimpleDateFormat("yyyy-MM-dd").parse((String) list.get(i).getNgaySinh());
                txtngaysinh.setDate(date);
            } catch (ParseException ex) {
                Logger.getLogger(Thaydoithongtin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }

