    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed

        int maCN = Integer.parseInt(txtMaCN.getText());
        String tenCN = txtHoTenCN.getText();
        String CMND = txtCMNDCN.getText();
        String sDT = txtSDT.getText();
        double luongcb = Double.parseDouble(txtLuongcb.getText());
        double phuCap = Double.parseDouble(txtPhuCapCN.getText());
        String diaChi = txtDiaChiCN.getText();
        String gioiTinh = cboGioiTinhCN.getSelectedItem().toString();
        String chucVu = cboChucVu.getSelectedItem().toString();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date date = txtngaysinh.getDate();
        String ngaySinh = sdf.format(date);
        CongNhan cn = new CongNhan(maCN, tenCN, CMND, sDT, luongcb, phuCap, diaChi, gioiTinh, chucVu, ngaySinh);
        cnDao.update(cn);
        updateData();
    }//GEN-LAST:event_btnsuaActionPerformed

